#!/usr/bin/env node
/**
 * compute-guide-dates.mjs
 *
 * For each guide file under src/data/agent-guides/<slug>.ts, run:
 *   git log -1 --format=%cI -- <path>
 * to get the last commit date (committer date, ISO 8601), then write a
 * frozen { slug: 'YYYY-MM-DD' } map to src/lib/guide-modified-dates.ts.
 *
 * Why: SeoJsonLd.tsx previously stamped `dateModified` with `new Date()`,
 * meaning every deploy reset all 22 guides to "today" — a suspicious signal
 * for Google. With per-guide modification dates, each guide's
 * dateModified reflects its actual last edit.
 *
 * Run manually: node scripts/compute-guide-dates.mjs
 * (Run before commit when guide content changes; output is committed.)
 */

import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { execFileSync } from 'node:child_process';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const PROJECT_ROOT = path.resolve(__dirname, '..');

const GUIDES_DIR = path.join(PROJECT_ROOT, 'src', 'data', 'agent-guides');
const OUTPUT_FILE = path.join(
  PROJECT_ROOT,
  'src',
  'lib',
  'guide-modified-dates.ts',
);

// Files in agent-guides that are NOT individual guides (shared types/index/en).
const SKIP_FILES = new Set(['index.ts']);

/**
 * Get last commit date (YYYY-MM-DD) for a file path via git.
 * Returns null if git fails or the file is untracked.
 */
function gitLastModified(filePath) {
  try {
    const out = execFileSync(
      'git',
      ['log', '-1', '--format=%cI', '--', filePath],
      {
        cwd: PROJECT_ROOT,
        encoding: 'utf8',
        stdio: ['ignore', 'pipe', 'ignore'],
      },
    ).trim();
    if (!out) return null;
    // %cI is full ISO 8601, e.g. "2026-04-15T12:34:56+03:00". Take the date part.
    return out.slice(0, 10);
  } catch {
    return null;
  }
}

async function main() {
  let files;
  try {
    files = await fs.readdir(GUIDES_DIR);
  } catch (err) {
    console.error(`[compute-guide-dates] Cannot read ${GUIDES_DIR}:`, err.message);
    process.exit(1);
  }

  const guideEntries = files
    .filter((f) => f.endsWith('.ts') && !SKIP_FILES.has(f))
    .map((f) => ({
      slug: f.replace(/\.ts$/, ''),
      filePath: path.join(GUIDES_DIR, f),
    }))
    .sort((a, b) => a.slug.localeCompare(b.slug));

  const dates = {};
  const today = new Date().toISOString().slice(0, 10);
  let resolved = 0;
  let fallbacks = 0;

  for (const { slug, filePath } of guideEntries) {
    const relPath = path.relative(PROJECT_ROOT, filePath).replace(/\\/g, '/');
    const date = gitLastModified(relPath);
    if (date) {
      dates[slug] = date;
      resolved += 1;
    } else {
      dates[slug] = today;
      fallbacks += 1;
    }
  }

  const banner = `// AUTO-GENERATED by scripts/compute-guide-dates.mjs — do not edit by hand.
// Last regenerated: ${today}
//
// Last-modified date (YYYY-MM-DD) for each agent guide, derived from
// \`git log -1 --format=%cI\` on src/data/agent-guides/<slug>.ts.
//
// Used by SeoJsonLd to emit accurate JSON-LD \`dateModified\` per guide,
// so Google does not see all guides "updated today" on every deploy.
`;

  const body = `export const GUIDE_MODIFIED: Readonly<Record<string, string>> = Object.freeze({\n${Object.entries(
    dates,
  )
    .map(([slug, date]) => `  ${JSON.stringify(slug)}: ${JSON.stringify(date)},`)
    .join('\n')}\n});\n`;

  await fs.mkdir(path.dirname(OUTPUT_FILE), { recursive: true });
  await fs.writeFile(OUTPUT_FILE, `${banner}\n${body}`, 'utf8');

  console.log(
    `[compute-guide-dates] Wrote ${guideEntries.length} entries to ${path.relative(
      PROJECT_ROOT,
      OUTPUT_FILE,
    )} (${resolved} from git, ${fallbacks} fallback to today)`,
  );
}

main().catch((err) => {
  console.error('[compute-guide-dates] Failed:', err);
  process.exit(1);
});
